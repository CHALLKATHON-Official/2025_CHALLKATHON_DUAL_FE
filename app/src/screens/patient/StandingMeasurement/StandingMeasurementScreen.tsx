import React, { useState, useEffect, useRef } from 'react';
import {
  View,
  Text,
  SafeAreaView,
  TouchableOpacity,
  Alert,
  ScrollView,
  Animated,
  ActivityIndicator,
} from 'react-native';
import { useNavigation } from '@react-navigation/native';
import { NativeStackNavigationProp } from '@react-navigation/native-stack';
import { Feather } from '@expo/vector-icons';
import Card from '../../../components/common/Card';
import { IndoorStackParamList } from '../../../navigation/types';
import { recordSimpleExercise } from '../../../api';
import { StandingMeasurementState } from './types';
import { 
  EXERCISE_CONFIG, 
  EXERCISE_STEPS, 
  EXERCISE_BENEFITS, 
  EXERCISE_CAUTIONS, 
  EXERCISE_INFO,
  PHASE_CONFIG 
} from './mock';
import { styles } from './StandingMeasurementScreen.styled';

const StandingMeasurementScreen: React.FC = () => {
  const navigation = useNavigation<NativeStackNavigationProp<IndoorStackParamList, 'StandingMeasurement'>>();
  const [state, setState] = useState<StandingMeasurementState>({
    started: false,
    currentSet: 0,
    currentRep: 0,
    isResting: false,
    restTimer: EXERCISE_CONFIG.restDuration,
    currentPhase: 'sitting',
  });
  const [isSubmitting, setIsSubmitting] = useState(false);
  const [startTime, setStartTime] = useState<number | null>(null);

  const progressAnim = useRef(new Animated.Value(0)).current;
  const intervalRef = useRef<NodeJS.Timeout | null>(null);

  useEffect(() => {
    return () => {
      if (intervalRef.current) {
        clearInterval(intervalRef.current);
      }
    };
  }, []);

  const handleStart = () => {
    setState(prev => ({
      ...prev,
      started: true,
      currentSet: 1,
      currentRep: 0,
      currentPhase: 'sitting',
      isResting: false,
    }));
    setStartTime(Date.now()); // ÏãúÏûë ÏãúÍ∞Ñ Í∏∞Î°ù
  };

  const handleRep = () => {
    if (state.currentPhase === 'sitting') {
      // ÏùºÏñ¥ÏÑúÍ∏∞ ÎèôÏûë
      setState(prev => ({ ...prev, currentPhase: 'standing' }));
    } else {
      // ÏïâÍ∏∞ ÎèôÏûë ÏôÑÎ£å - 1Ìöå ÏôÑÎ£å
      const newRep = state.currentRep + 1;
      setState(prev => ({ ...prev, currentRep: newRep, currentPhase: 'sitting' }));

      // ÏßÑÌñâÎ•† Ïï†ÎãàÎ©îÏù¥ÏÖò
      const progress = newRep / EXERCISE_CONFIG.repsPerSet;
      Animated.timing(progressAnim, {
        toValue: progress,
        duration: 300,
        useNativeDriver: false,
      }).start();

      if (newRep >= EXERCISE_CONFIG.repsPerSet) {
        // Ìïú ÏÑ∏Ìä∏ ÏôÑÎ£å
        if (state.currentSet >= EXERCISE_CONFIG.totalSets) {
          // Î™®Îì† ÏÑ∏Ìä∏ ÏôÑÎ£å - API Ìò∏Ï∂ú
          const completeExercise = async () => {
            try {
              setIsSubmitting(true);
              
              const totalDuration = startTime ? Math.max(1, Math.floor((Date.now() - startTime) / 60000)) : 3; // Î∂Ñ Îã®ÏúÑ
              
              const exerciseRecord = {
                exerciseId: 4, // ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô ID  
                durationMinutes: totalDuration,
                notes: `ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô ${EXERCISE_CONFIG.totalSets}ÏÑ∏Ìä∏ (${EXERCISE_CONFIG.repsPerSet}Ìöå/ÏÑ∏Ìä∏) ÏôÑÎ£å`
              };

              console.log('ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô Í∏∞Î°ù Ï†ÑÏÜ°:', exerciseRecord);
              
              const result = await recordSimpleExercise(exerciseRecord);
              console.log('ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô Í∏∞Î°ù ÏÑ±Í≥µ:', result);

              Alert.alert(
                'Ïö¥Îèô ÏôÑÎ£å! üéâ',
                `ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô ${EXERCISE_CONFIG.totalSets}ÏÑ∏Ìä∏Î•º Î™®Îëê ÏôÑÎ£åÌïòÏÖ®ÏäµÎãàÎã§!\n\nÌïòÏ≤¥ Í∑ºÎ†•Ïù¥ ÌÅ¨Í≤å Ìñ•ÏÉÅÎêòÏóàÏñ¥Ïöî.`,
                [
                  {
                    text: 'ÌôïÏù∏',
                    onPress: () => {
                      setState(prev => ({ ...prev, started: false }));
                      navigation.navigate('HealthCheck', {
                        exerciseName: 'ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô',
                        exerciseType: 'standing',
                        exerciseData: {
                          duration: totalDuration * 60,
                          sets: EXERCISE_CONFIG.totalSets,
                          reps: EXERCISE_CONFIG.repsPerSet
                        }
                      });
                    },
                  },
                ]
              );
            } catch (error) {
              console.error('ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô Í∏∞Î°ù Ï†ÄÏû• Ïã§Ìå®:', error);
              
              Alert.alert(
                'Ïö¥Îèô ÏôÑÎ£å! üéâ',
                `ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô ${EXERCISE_CONFIG.totalSets}ÏÑ∏Ìä∏Î•º Î™®Îëê ÏôÑÎ£åÌïòÏÖ®ÏäµÎãàÎã§!\n\nÍ∏∞Î°ù Ï†ÄÏû•ÏóêÎäî Ïã§Ìå®ÌñàÏßÄÎßå Í±¥Í∞ï Ï≤¥ÌÅ¨Îäî Í≥ÑÏÜç ÏßÑÌñâÎê©ÎãàÎã§.`,
                [
                  {
                    text: 'ÌôïÏù∏',
                    onPress: () => {
                      setState(prev => ({ ...prev, started: false }));
                      navigation.navigate('HealthCheck', {
                        exerciseName: 'ÏÑúÏÑúÌïòÍ∏∞ Ïö¥Îèô',
                        exerciseType: 'standing',
                        exerciseData: {
                          duration: 3 * 60, // Í∏∞Î≥∏Í∞í 3Î∂Ñ
                          sets: EXERCISE_CONFIG.totalSets,
                          reps: EXERCISE_CONFIG.repsPerSet
                        }
                      });
                    },
                  },
                ]
              );
            } finally {
              setIsSubmitting(false);
            }
          };
          
          completeExercise();
        } else {
          // Îã§Ïùå ÏÑ∏Ìä∏Î°ú
          setState(prev => ({
            ...prev,
            currentSet: prev.currentSet + 1,
            currentRep: 0,
          }));
          progressAnim.setValue(0);
          startRestTimer(EXERCISE_CONFIG.restDuration);
        }
      }
    }
  };

  const startRestTimer = (seconds: number) => {
    setState(prev => ({ ...prev, isResting: true, restTimer: seconds }));
    
    intervalRef.current = setInterval(() => {
      setState(prev => {
        if (prev.restTimer <= 1) {
          if (intervalRef.current) clearInterval(intervalRef.current);
          return { ...prev, isResting: false, restTimer: seconds };
        }
        return { ...prev, restTimer: prev.restTimer - 1 };
      });
    }, 1000);
  };

  const handleStop = () => {
    if (intervalRef.current) {
      clearInterval(intervalRef.current);
    }
    Alert.alert(
      'Ïö¥Îèô Ï§ëÎã®',
      'Ï†ïÎßê Ïö¥ÎèôÏùÑ Ï§ëÎã®ÌïòÏãúÍ≤†ÏäµÎãàÍπå?',
      [
        { text: 'Í≥ÑÏÜçÌïòÍ∏∞', style: 'cancel' },
        {
          text: 'Ï§ëÎã®ÌïòÍ∏∞',
          style: 'destructive',
          onPress: () => {
            setState({
              started: false,
              currentSet: 0,
              currentRep: 0,
              isResting: false,
              restTimer: EXERCISE_CONFIG.restDuration,
              currentPhase: 'sitting',
            });
            progressAnim.setValue(0);
          },
        },
      ]
    );
  };

  const handleGoBack = () => {
    if (state.started) {
      Alert.alert(
        'Ïö¥Îèô Ï§ëÎã®',
        'Ïö¥ÎèôÏùÑ Ï§ëÎã®ÌïòÍ≥† Ïù¥Ï†Ñ ÌôîÎ©¥ÏúºÎ°ú ÎèåÏïÑÍ∞ÄÏãúÍ≤†ÏäµÎãàÍπå?',
        [
          { text: 'Í≥ÑÏÜçÌïòÍ∏∞', style: 'cancel' },
          {
            text: 'ÎÇòÍ∞ÄÍ∏∞',
            style: 'destructive',
            onPress: () => navigation.goBack(),
          },
        ]
      );
    } else {
      navigation.goBack();
    }
  };

  const getTotalProgress = () => 
    ((state.currentSet - 1) * EXERCISE_CONFIG.repsPerSet + state.currentRep) / 
    (EXERCISE_CONFIG.totalSets * EXERCISE_CONFIG.repsPerSet) * 100;

  const currentPhaseConfig = PHASE_CONFIG[state.currentPhase];

  return (
    <SafeAreaView style={styles.container}>
      {/* Header */}
      <View style={styles.header}>
        <TouchableOpacity style={styles.backButton} onPress={handleGoBack}>
          <Feather name="chevron-left" size={28} color="#A3A8AF" />
        </TouchableOpacity>
        <View style={styles.headerTextContainer}>
          <Text style={styles.headerTitle}>{EXERCISE_INFO.title}</Text>
          <Text style={styles.headerSubtitle}>{EXERCISE_INFO.subtitle}</Text>
        </View>
        {state.started && (
          <TouchableOpacity style={styles.stopButton} onPress={handleStop}>
            <Feather name="x" size={24} color="#EF4444" />
          </TouchableOpacity>
        )}
      </View>

      <ScrollView contentContainerStyle={styles.scrollContent} showsVerticalScrollIndicator={false}>
        {!state.started ? (
          <>
            {/* Ïö¥Îèô ÏÜåÍ∞ú Ïπ¥Îìú */}
            <View style={styles.section}>
              <Card style={styles.introCard}>
                <View style={styles.introHeader}>
                  <View style={styles.iconContainer}>
                    <Text style={styles.iconText}>{EXERCISE_INFO.emoji}</Text>
                  </View>
                  <View style={styles.introInfo}>
                    <Text style={styles.introTitle}>{EXERCISE_INFO.title}</Text>
                    <Text style={styles.introDescription}>{EXERCISE_INFO.description}</Text>
                  </View>
                </View>
                <View style={styles.exerciseStats}>
                  <View style={styles.statItem}>
                    <Text style={styles.statValue}>{EXERCISE_CONFIG.totalSets}</Text>
                    <Text style={styles.statLabel}>ÏÑ∏Ìä∏</Text>
                  </View>
                  <View style={styles.statDivider} />
                  <View style={styles.statItem}>
                    <Text style={styles.statValue}>{EXERCISE_CONFIG.repsPerSet}</Text>
                    <Text style={styles.statLabel}>Ìöå/ÏÑ∏Ìä∏</Text>
                  </View>
                  <View style={styles.statDivider} />
                  <View style={styles.statItem}>
                    <Text style={styles.statValue}>{EXERCISE_CONFIG.estimatedDuration}</Text>
                    <Text style={styles.statLabel}>ÏÜåÏöîÏãúÍ∞Ñ</Text>
                  </View>
                </View>
              </Card>
            </View>

            {/* Ïö¥Îèô Î∞©Î≤ï */}
            <View style={styles.section}>
              <Text style={styles.sectionTitle}>Ïö¥Îèô Î∞©Î≤ï</Text>
              <Card style={styles.methodCard}>
                <View style={styles.stepList}>
                  {EXERCISE_STEPS.map((step) => (
                    <View key={step.id} style={styles.stepItem}>
                      <View style={styles.stepNumber}>
                        <Text style={styles.stepNumberText}>{step.id}</Text>
                      </View>
                      <Text style={styles.stepText}>{step.description}</Text>
                    </View>
                  ))}
                </View>
              </Card>
            </View>

            {/* Ïö¥Îèô Ìö®Í≥º & Ï£ºÏùòÏÇ¨Ìï≠ */}
            <View style={styles.twoColumnSection}>
              <View style={styles.halfWidth}>
                <Text style={styles.sectionTitle}>Ïö¥Îèô Ìö®Í≥º</Text>
                <Card style={styles.benefitCard}>
                  {EXERCISE_BENEFITS.map((benefit, index) => (
                    <View key={index} style={styles.benefitItem}>
                      <Text style={styles.benefitIcon}>{benefit.icon}</Text>
                      <Text style={styles.benefitText}>{benefit.text}</Text>
                    </View>
                  ))}
                </Card>
              </View>

              <View style={styles.halfWidth}>
                <Text style={styles.sectionTitle}>Ï£ºÏùòÏÇ¨Ìï≠</Text>
                <Card style={styles.cautionCard}>
                  {EXERCISE_CAUTIONS.map((caution, index) => (
                    <View key={index} style={styles.cautionItem}>
                      <Text style={styles.cautionIcon}>{caution.icon}</Text>
                      <Text style={styles.cautionText}>{caution.text}</Text>
                    </View>
                  ))}
                </Card>
              </View>
            </View>

            {/* ÏãúÏûë Î≤ÑÌäº */}
            <View style={styles.section}>
              <TouchableOpacity style={styles.startButton} onPress={handleStart}>
                <Text style={styles.startButtonText}>Ïö¥Îèô ÏãúÏûëÌïòÍ∏∞</Text>
                <Feather name="play" size={20} color="#FFFFFF" style={styles.startButtonIcon} />
              </TouchableOpacity>
            </View>
          </>
        ) : (
          <>
            {/* ÏßÑÌñâ ÏÉÅÌô© Ïπ¥Îìú */}
            <View style={styles.section}>
              <Card style={styles.progressCard}>
                <View style={styles.progressHeader}>
                  <Text style={styles.progressTitle}>
                    {state.currentSet}ÏÑ∏Ìä∏ - {state.currentRep + 1}Î≤àÏß∏
                  </Text>
                  <Text style={styles.progressSubtitle}>
                    {EXERCISE_CONFIG.totalSets}ÏÑ∏Ìä∏ Ï§ë {state.currentSet}ÏÑ∏Ìä∏ ÏßÑÌñâ Ï§ë
                  </Text>
                </View>
                
                <View style={styles.progressBarContainer}>
                  <View style={styles.progressBar}>
                    <View style={[styles.progressBarFill, { width: `${getTotalProgress()}%` }]} />
                  </View>
                  <Text style={styles.progressPercent}>{Math.round(getTotalProgress())}%</Text>
                </View>
              </Card>
            </View>

            {/* ÌòÑÏû¨ ÏÉÅÌÉú Ïπ¥Îìú */}
            <View style={styles.section}>
              <Card style={styles.statusCard}>
                {state.isResting ? (
                  <View style={styles.statusContent}>
                    <Text style={styles.statusTitle}>Ìú¥Ïãù ÏãúÍ∞Ñ</Text>
                    <Text style={styles.statusSubtitle}>Îã§Ïùå ÏÑ∏Ìä∏ÍπåÏßÄ</Text>
                    <Text style={styles.timerText}>{state.restTimer}Ï¥à</Text>
                    <Text style={styles.statusInstruction}>
                      Ï∂©Î∂ÑÌûà Ìú¥ÏãùÏùÑ Ï∑®ÌïòÍ≥† Îã§Ïùå ÏÑ∏Ìä∏Î•º Ï§ÄÎπÑÌïòÏÑ∏Ïöî
                    </Text>
                  </View>
                ) : (
                  <View style={styles.statusContent}>
                    <Text style={styles.statusTitle}>{currentPhaseConfig.text}</Text>
                    <Text style={styles.statusSubtitle}>
                      {state.currentRep}/{EXERCISE_CONFIG.repsPerSet}Ìöå ÏôÑÎ£å
                    </Text>
                    
                    <View style={styles.exerciseIconContainer}>
                      <Text style={styles.currentExerciseIcon}>
                        {currentPhaseConfig.emoji}
                      </Text>
                    </View>

                    <View style={styles.animationBarContainer}>
                      <Animated.View
                        style={[
                          styles.animationBar,
                          {
                            width: progressAnim.interpolate({
                              inputRange: [0, 1],
                              outputRange: ['0%', '100%'],
                            }),
                          },
                        ]}
                      />
                    </View>
                    
                    <View style={styles.instructionContainer}>
                      <Text style={styles.instructionText}>
                        {currentPhaseConfig.instruction}
                      </Text>
                    </View>
                    
                    <TouchableOpacity style={styles.repButton} onPress={handleRep}>
                      <Text style={styles.repButtonText}>{currentPhaseConfig.action}</Text>
                    </TouchableOpacity>
                  </View>
                )}
              </Card>
            </View>
          </>
        )}
      </ScrollView>
    </SafeAreaView>
  );
};

export default StandingMeasurementScreen;
